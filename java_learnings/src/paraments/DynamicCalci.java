public class DynamicCalci {

	public int  add(int a, int b)
	{
		int c=a+b;
//
		return c;
	}
	
	public double sub(double a, double b)
	{
		double c=b-a;
		return c;	
	}

	
	public static  String fullname(String first, String lastname)
	{
	String fullname= first+lastname;
	return fullname;
	}
}
